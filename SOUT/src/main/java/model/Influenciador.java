package model;

public class Influenciador {

	private Integer id_influenciador;
	private Integer id_usuario;;
	private String nmInfluenciador;
	private String dtInfluenciador;
	
	public Integer getId_influenciador() {
		return id_influenciador;
	}
	public void setId_influenciador(Integer id_influenciador) {
		this.id_influenciador = id_influenciador;
	}
	public Integer getId_usuario() {
		return id_usuario;
	}
	public void setId_usuario(Integer id_usuario) {
		this.id_usuario = id_usuario;
	}
	public String getNmInfluenciador() {
		return nmInfluenciador;
	}
	public void setNmInfluenciador(String nmInfluenciador) {
		this.nmInfluenciador = nmInfluenciador;
	}
	public String getDtInfluenciador() {
		return dtInfluenciador;
	}
	public void setDtInfluenciador(String dtInfluenciador) {
		this.dtInfluenciador = dtInfluenciador;
	}
	

}
